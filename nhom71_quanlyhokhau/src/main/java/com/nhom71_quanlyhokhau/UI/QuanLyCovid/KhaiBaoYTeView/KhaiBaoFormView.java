        KhaiBaoBean khaiBaoBean = new KhaiBaoBean();
        KhaiBao khaiBao = new KhaiBao();
        TrieuChung trieuChung = new TrieuChung();
        List<LichTrinh> lichTrinhList = new ArrayList<>();
        NhanKhau nhanKhau = new NhanKhau();

        khaiBao.setIdNhanKhau(Integer.parseInt(jTextIdNhanKhau.getText()));
        khaiBao.setSoDienThoai(jTextIdSdt.getText());
        khaiBao.setNoiOHienTai(jTextIdNoiOHienTai.getText());
        khaiBaoBean.setKhaiBao(khaiBao);

        nhanKhau.setId(Integer.parseInt(jTextIdNhanKhau.getText()));
        khaiBaoBean.setNhanKhau(nhanKhau);

        String bieuHienBenh = "";
        if (jCheckBoxSot.isSelected()) bieuHienBenh+= "Sốt, ";
        if (jCheckHo.isSelected()) bieuHienBenh+= "Ho, ";
        if (jCheckKhoTho.isSelected()) bieuHienBenh+= "Khó thở, ";
        if (jCheckViemPhoi.isSelected()) bieuHienBenh+= "Viêm phổi, ";
        if (jCheckDauHong.isSelected()) bieuHienBenh+= "Đau họng, ";
        if (jCheckMetMoi.isSelected()) bieuHienBenh+= "Mệt mỏi, ";
        String trieuChungInput = "";
        if (!bieuHienBenh.isEmpty()) trieuChungInput = bieuHienBenh.substring(0, bieuHienBenh.length() - 2);
