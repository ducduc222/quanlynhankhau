        khaiBaoBean.setTrieuChung(trieuChung);

        for (int i = 0; i<danhSachLichTrinh.getRowCount(); i++) {
            if (danhSachLichTrinh.getValueAt(i, 0) == null) break;
            LichTrinh lichTrinh = new LichTrinh();
            lichTrinh.setNgay((String) danhSachLichTrinh.getValueAt(i, 0));
            lichTrinh.setNoiDung((String) danhSachLichTrinh.getValueAt(i, 1));

            lichTrinhList.add(lichTrinh);
        }
        khaiBaoBean.setLichTrinhList(lichTrinhList);

        ThemKhaiBaoCtrl themKhaiBaoCtrl = new ThemKhaiBaoCtrl();
        if (themKhaiBaoCtrl.addNewKhaiBao(khaiBaoBean)) {
            JOptionPane.showMessageDialog(null, "Thêm thành công");
            dispose();
        }
        return;
