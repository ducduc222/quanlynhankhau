    private void jButtonLuuActionPerformed(java.awt.event.ActionEvent evt) throws SQLException, ClassNotFoundException {//GEN-FIRST:event_jButtonLuuActionPerformed
        // TODO add your handling code here:
        if (    jTextHoTen.getText().isEmpty()
                ||  jTextSdt.getText().isEmpty()
                ||  jTextNoiOHienTai.getText().isEmpty()
                ||  jTextKetQua.getText().isEmpty()
        ) {
            JOptionPane.showMessageDialog(null, "Hãy nhập đủ thông tin");
            return;
        }

        TestCovidBean testCovidBean = new TestCovidBean();
        NhanKhau nhanKhau = new NhanKhau();
        TestCovid testCovid = new TestCovid();

        nhanKhau.setId(Integer.parseInt(jTextIdNhanKhau.getText()));
        nhanKhau.setHoTen(jTextHoTen.getText());
        testCovidBean.setNhanKhau(nhanKhau);

        testCovid.setIdNhanKhau(Integer.parseInt(jTextIdNhanKhau.getText()));
        testCovid.setSoDienThoai(jTextSdt.getText());
        testCovid.setNoiOHienTai(jTextNoiOHienTai.getText());
        testCovid.setNgayTest(DateForm.returnDateString(jDateNgayTest.getDate()));
        testCovid.setKetQuaTest(jTextKetQua.getText());
        testCovidBean.setTestCovid(testCovid);

        ThemTestCovidCtrl themTestCovidCtrl = new ThemTestCovidCtrl();
        if (themTestCovidCtrl.addNewTestCovid(testCovidBean)) {
            JOptionPane.showMessageDialog(null, "Thêm thành công");
            dispose();
        }

    }//GEN-LAST:event_jButtonLuuActionPerformed

