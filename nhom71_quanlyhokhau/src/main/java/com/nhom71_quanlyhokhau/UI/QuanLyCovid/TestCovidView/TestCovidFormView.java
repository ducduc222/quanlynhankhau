    private void jButtonCheckActionPerformed(java.awt.event.ActionEvent evt) throws SQLException, ClassNotFoundException {//GEN-FIRST:event_jButtonCheckActionPerformed
        // TODO add your handling code here:
        String cccd = jTextCCCD.getText();
        if (CheckCccdCtrl.returnNhanKhau(cccd) == 0) return;
        int idNhanKhau = CheckCccdCtrl.returnNhanKhau(cccd);

        NhanKhauCtrl nhanKhauCtrl = new NhanKhauCtrl();
        NhanKhauBean nhanKhauBean = nhanKhauCtrl.getChiTietNhanKhau(idNhanKhau);

        jTextIdNhanKhau.setText(String.valueOf(nhanKhauBean.getNhanKhau().getId()));
        jTextHoTen.setText(nhanKhauBean.getNhanKhau().getHoTen());
        jTextIdGioiTinh.setText(nhanKhauBean.getNhanKhau().getGioiTinh());
        jTextIdNgaySinh.setText(nhanKhauBean.getNhanKhau().getNgaySinh());

    }//GEN-LAST:event_jButtonCheckActionPerformed

