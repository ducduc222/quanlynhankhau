    private void loadData() throws SQLException, ClassNotFoundException {
        NhanKhauCtrl nhanKhauCtrl = new NhanKhauCtrl();
        NhanKhauBean nhanKhauBean = nhanKhauCtrl.getChiTietNhanKhau(idNhanKhau);

        jLabelIdNhanKhau.setText(String.valueOf(nhanKhauBean.getNhanKhau().getId()));
        jLabelHoten.setText(nhanKhauBean.getNhanKhau().getHoTen());
        jLabelNgaySinh.setText(nhanKhauBean.getNhanKhau().getNgaySinh());
        jLabelGioiTinh.setText(nhanKhauBean.getNhanKhau().getGioiTinh());
        jLabelNoiSinh.setText(nhanKhauBean.getNhanKhau().getNoiSinh());
        jLabelNguyenQuan.setText(nhanKhauBean.getNhanKhau().getNguyenQuan());
        jLabelDanToc.setText(nhanKhauBean.getNhanKhau().getDanToc());
        jLabelTonGiao.setText(nhanKhauBean.getNhanKhau().getTonGiao());
        jLabelQuocTich.setText(nhanKhauBean.getNhanKhau().getQuocTich());
        jLabelNoiThuongTru.setText(nhanKhauBean.getNhanKhau().getNoiThuongTru());
        jLabelDiaChiHienTai.setText(nhanKhauBean.getNhanKhau().getDiaChiHienTai());
        jLabelTrinhDoHocVan.setText(nhanKhauBean.getNhanKhau().getTrinhDoHocVan());
        jLabelNgheNghiep.setText(nhanKhauBean.getNhanKhau().getNgheNghiep());
        jLabelNoiLamViec.setText(nhanKhauBean.getNhanKhau().getNoiLamViec());

        ChungMinhNhanDan chungMinhNhanDan = new ChungMinhNhanDan();
        if (nhanKhauBean.getChungMinhNhanDan()!=null) {
            chungMinhNhanDan = nhanKhauBean.getChungMinhNhanDan();
            jLabelCCCD.setText(chungMinhNhanDan.getSoCMND());
        }
        // load gia đình
        List<GiaDinh> giaDinhList = nhanKhauBean.getDanhSachThanhVienGiaDinh();
        for (int i = 0; i<giaDinhList.size(); i++) {
            String hoTen = giaDinhList.get(i).getHoTen();
            String ngaySinh = giaDinhList.get(i).getNgaySinh();
            String gioiTinh = giaDinhList.get(i).getGioiTinh();
            String quanhevoinhankhau = giaDinhList.get(i).getQuanHeVoiNhanKhau();

            Object[] row = {hoTen,gioiTinh, ngaySinh, quanhevoinhankhau};
            danhSachGiaDinh.addRow(row);
        }

    }


